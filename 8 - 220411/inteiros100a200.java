/*
 * +Faça laços em Java usando while para mostrar:
    +Os números inteiros de 100 a 200
 */
public class inteiros100a200
{
    public static void main(String[] args)
    {
        int intInicial = 100;
        
        while (intInicial <= 200){
            System.out.println(intInicial);
            intInicial++;
        }
    }
}

